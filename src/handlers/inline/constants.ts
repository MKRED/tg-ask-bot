// Inline-режим поиска картинок (см. handlers/inline/index.ts)

// Минимальная длина запроса, при которой ищем по эмбеддингу.
// Короче — отдаём browse-выдачу (случайные картинки).
export const INLINE_MIN_QUERY_LEN = 2;

// Размер одной страницы выдачи. Telegram отдаёт до 50 inline-результатов за раз;
// 25 — компромисс между «много за прокрутку» и весом ответа.
export const INLINE_PAGE_SIZE = 25;

// Потолок глубины пагинации в search-режиме. Хвост выдачи (от ближайшей картинки
// к самой далёкой по всей базе) — наименее релевантные результаты, а каждая глубокая
// страница — полная пересортировка таблицы по расстоянию (индекса на 3072-dim векторе
// нет → exact NN). Дальше потолка next_offset не ставим — Telegram перестаёт листать.
export const INLINE_MAX_RESULTS = 250;

// Сколько случайных картинок показываем на пустой запрос (browse-режим).
export const INLINE_BROWSE_COUNT = 25;

// cache_time для answerInlineQuery (сек). Низкий: вектор закэширован, поиск дешёвый,
// а низкий cache_time не мешает Telegram быстро подхватывать обновления выдачи.
export const INLINE_CACHE_TIME = 5;
