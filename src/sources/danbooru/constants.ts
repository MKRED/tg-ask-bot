// Базовый адрес Danbooru — общий для HTTP-клиента (client.ts) и построения ссылок на посты (transform.ts).
export const DANBOORU_BASE_URL = "https://danbooru.donmai.us";

// Интервал между тиками воркера (мс). Применяется ТОЛЬКО когда воркер догнал фронт
// (постов нет или упёрлись в возрастной гейт). При наличии бэклога (батч пришёл полным)
// тики идут вплотную — см. логику переустановки таймера в worker.ts.
export const DANBOORU_TICK_MS = 15_000;
// Количество постов Danbooru за один тик (лимит API — 100). Берём крупный батч, чтобы
// держать пул обработки (DANBOORU_CONCURRENCY) загруженным и реже ходить за окном.
export const DANBOORU_BATCH_SIZE = 80;
// Степень параллелизма обработки постов в одном тике (download→embed→upload→save на пост).
// Прячет сетевые ожидания одних постов за работой других. Держим умеренной: эмбеддинги
// идут по тому же Gemini-ключу, что и ingest-воркер (3 параллельно), а все запросы —
// через один прокси. autoRetry в bot.ts гасит редкие 429 от sendPhoto; если 429 станут
// частыми в логах — значит упёрлись в per-chat лимит Telegram, тогда снижаем или шардим
// загрузку по нескольким storage-чатам.
export const DANBOORU_CONCURRENCY = 2;
// Размер пачки для отправки картинок альбомом (sendMediaGroup) — чтобы не спамить в чат
// по одной картинке (каждая = отдельное сообщение + уведомление). Альбом из N фото = одно
// уведомление. Telegram допускает media group из 2–10; берём 5 как баланс «меньше спама /
// не слишком тяжёлый альбом». Пачка из 1 (хвост/retry-скрипт) уходит обычным sendPhoto
// (sendMediaGroup требует ≥2). См. upload.ts.
export const DANBOORU_UPLOAD_BATCH_SIZE = 5;
// Проактивный пейсер загрузок: минимальный интервал на ОДНУ картинку при отправке в storage-чат,
// общий для всех параллельных воркеров (upload.awaitUploadSlot). Альбом весит как N картинок
// (пейсер сдвигает слот на N × интервал) → эффективный темп фото/мин такой же, как при поштучной
// отправке. Под per-chat flood-лимит Telegram (~20/мин для групп). autoRetry в bot.ts настроен с
// бесконечными попытками (maxRetryAttempts по умолчанию = Infinity) → даже при превышении лимита
// 429 просто пережидается, посты НЕ падают в failed; пейсер лишь не даёт молотить лимит и копить
// floodwait'ы. Если в логах нет 429 — можно уменьшить (быстрее); если есть — увеличить.
export const DANBOORU_UPLOAD_MIN_INTERVAL_MS = 2_000;
// Пауза между загрузками для СЕРИЙНОГО скрипта восстановления (retryFailedDanbooru.ts).
// Фоновый воркер её не использует — там темп задаёт пейсер выше.
export const DANBOORU_UPLOAD_DELAY_MS = 1_500;
// Sentinel senderUserId для saved_images, импортированных с Danbooru
// (Telegram ID всегда > 0, поэтому 0 означает «не живой пользователь»)
export const DANBOORU_SENDER_ID = 0;
// Поддерживаемые расширения. GIF/WebM/MP4 пропускаем: Telegram принимает их только как
// animation/document, а не как photo — значит InlineQueryResultCachedPhoto не заработает.
export const DANBOORU_ALLOWED_EXTS = new Set(["jpg", "jpeg", "png", "webp"]);
// Минимальный «возраст» поста перед загрузкой (мс). У свежих постов score ещё не сформирован
// (0–1 сразу после публикации), поэтому фильтр по оценке был бы недостоверен. По эмпирическому
// анализу score созревает за ~2 суток (медиана 2→5, дальше плато), поэтому ждём 48ч. Курсор
// при этом трейлит на ~48ч позади реального времени.
export const DANBOORU_MIN_AGE_MS = 48 * 60 * 60 * 1000;
// Минимальный score «настоявшегося» поста. Ниже — «работа без отклика»: на 48ч ~половина
// постов имеет score < 5 (медиана ровно 5). T=5 отсекает нижнюю половину, оставляя посты
// с реальными голосами. Анализ распределения см. в истории проекта.
export const DANBOORU_MIN_SCORE = 5;
